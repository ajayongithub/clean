<p>

<h3>Oyepages.com - Social networking platform: Powered By Open Source Yii Framework & MongoDB</h3>

OyePages is a perfect social website to bring people together. Other than sharing content or meeting new friends - people of all ages and backgrounds can discover things that can truly enrich their lives. OyePages helps you connect with like-minded people for social welfare and awareness programs, or religious and cultural activities. Get everyone talking through groups, communities, forums, chats and activity streams. Join various local or global activities or interests in your locality or anywhere else in the world. OyePages opens new doors to revenue and involvement for local businesses and services. You can easily promote it in your targeted geographical area, and/or interact with your customers to gain greater loyalty, following and higher customer satisfaction. OyePages helps to grow your network into a more active and fulfilling social exchange. It facilitates its users with common interests and hobbies to find each other and enable meaningful exploration of thoughts, opinions, expressions and experiences with friends, contacts or rest of the world.

OPAC Labs team with 3 developers with 6 months of efforts delivered the complete solution. The Open Source technologies used for the foundations are PHP, YII Framework, LAMP and MongoDB, Twitter Bootstrap, JQuery, RIA,Knockout.js, Node.js
<p>
<h4>Our guidelines </h4>
Leverage Open Source technologies:
Apache, Yii Framework, PHP, MySQL, MongoDB, Linux, JQuery were perfect fit for social network needs, developer friendly
</p>
</p>
<h5>Scale out architecture:</h5>
Scale horizontally - scale the resources as you need.
To scale horizontally (or scale out) means to add more nodes to a system, such as adding a new computer to a distributed software application. An example might be scaling out from 1 web server to 3.

<p>
<h5>Performance</h5>
High perform ant data and application architecture using:

To use No-SQL data stores with in built data sharding capabilities and scale out data architecture - The reason we have chosed mongodb. 
Further MongoDB GridFS allows us to build cost effective and scale out cloud based  document/image CDN (content delivery network).
The MongoDB GRIDFS stores the documents across 1 to n servers and its performance is typically faster than any native operating system filesystem
</p>
<p>
<h5>Leverage Memcache - The Low cost distribute caching layer to add caching support across application</h5>

<h5>Data sharding</h5>
Across various nodes for load balancing and distributing reads and writes
</p>
<p>
<h5>Modern framework best practices</h5>
Modular, Conventions over configurations, Do-Not Repeat yourself principles
</p>

<h4>Why we chose YII Framework and PHP?</h4>
The Fast, Secure and Professional PHP Framework - Yii is a high-performance PHP framework best for developing Web 2.0 applications.

Yii comes with rich features: MVC, DAO/ActiveRecord, I18N/L10N, caching, authentication and role-based access control, scaffolding, testing, etc. It can reduce your development time significantly.

<h5>Fast</h5>
Yii only loads the features that you need. It has powerful caching support. It is explicitly designed to work efficiently with AJAX.

<h5>Secure</h5>
Security comes as standard with Yii. It includes input validation, output filtering, SQL injection and Cross-site scripting prevention.

<h5>Professional</h5>
Yii helps you develop clean and reusable code. It follows the MVC pattern, ensuring a clear separation of logic and presentation.


<h4>Why we chose MongoDB?</h4>
MongoDB is a document database that provides high performance, high availability, and easy scalability.

<h6>Document Database</h6>
Documents (objects) map nicely to programming language data types.

<h6>Embedded documents and arrays reduce need for joins</h6>

<h6>Dynamic schema makes polymorphism easier.</h6>

<h6>High Performance</h6>

Embedding makes reads and writes fast.

Indexes can include keys from embedded documents and arrays.

Optional streaming writes (no acknowledgments).

High Availability

Replicated servers with automatic master failover.

Easy Scalability

Automatic sharding distributes collection data across machines.

Eventually-consistent reads can be distributed over replicated servers.

<blockquote>MongoDB wasn’t designed in a lab. We built MongoDB from our own experiences building large scale, high availability, robust systems. We didn’t start from scratch, we really tried to figure out what was broken, and tackle that. So the way I think about MongoDB is that if you take MySql, and change the data model from relational to document based, you get a lot of great features: embedded docs for speed, manageability, agile development with schema-less databases, easier horizontal scalability because joins aren’t as important. There are lots of things that work great in relational databases: indexes, dynamic queries and updates to name a few, and we haven’t changed much there. For example, the way you design your indexes in MongoDB should be exactly the way you do it in MySql or Oracle, you just have the option of indexing an embedded field.
—Eliot Horowitz, 10gen CTO and Co-founder
</blockquote>

<p>
New database technologies are needed to facilitate horizontal scaling of the data layer, easier development, and the ability to store order(s) of magnitude more data than was used in the past.

A non-relational approach is the best path to database solutions which scale horizontally to many machines.

It is unacceptable if these new technologies make writing applications harder. Writing code should be faster, easier, and more agile.

The document data model (JSON/BSON) is easy to code to, easy to manage(dynamic schema), and yields excellent performance by grouping relevant data together internally.

It is important to keep deep functionality to keep programming fast and simple. While some things must be left out, keep as much as possible – for example secondaries indexes, unique key constraints, atomic operations, multi-document updates.
Database technology should run anywhere, being available both for running on your own servers or VMs, and also as a cloud pay-for-what-you-use service.
</p>

<p>
<h3>Key MongoDB Features</h3>
MongoDB focuses on flexibility, power, speed, and ease of use:
Flexibility

MongoDB stores data in JSON documents (which we serialize to BSON). JSON provides a rich data model that seamlessly maps to native programming language types, and the dynamic schema makes it easier to evolve your data model than with a system with enforced schemas such as a RDBMS.
Power

MongoDB provides a lot of the features of a traditional RDBMS such as secondary indexes, dynamic queries, sorting, rich updates, upserts (update if document exists, insert if it doesn’t), and easy aggregation. This gives you the breadth of functionality that you are used to from an RDBMS, with the flexibility and scaling capability that the non-relational model allows.

Speed/Scaling
By keeping related data together in documents, queries can be much faster than in a relational database where related data is separated into multiple tables and then needs to be joined later. MongoDB also makes it easy to scale out your database. 

Autosharding allows you to scale your cluster linearly by adding more machines. It is possible to increase capacity without any downtime, which is very important on the web when load can increase suddenly and bringing down the website for extended maintenance can cost your business large amounts of revenue.
Ease of use

MongoDB works hard to be very easy to install, configure, maintain, and use. To this end, MongoDB provides few configuration options, and instead tries to automatically do the “right thing” whenever possible. This means that MongoDB works right out of the box, and you can dive right into developing your application, instead of spending a lot of time fine-tuning obscure database configurations.

What happens when users grow - from lakhs to millions (database sharding / performance)
Although MongoDB supports a “standalone” or single-instance operation, production MongoDB deployments are distributed by default. Replica sets provide high performance replication with automated failover, while sharded clusters make it possible to partition large data sets over many machines transparently to the users. MongoDB users combine replica sets and sharded clusters to provide high levels redundancy for large data sets transparently for applications.
</p>



