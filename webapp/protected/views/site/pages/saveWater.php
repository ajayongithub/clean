            <h2>What you can do at home</h2>
            <ul>
            <li> Never put water down the drain when there may beanother use for it such as watering a plant or garden, or cleaning.</li>
            <li> Verify that your home is leak-free because many homes have hidden water leaks. Read your water meter before and after a two-hour period when no water is being used. If the meter does not read exactly the same, there is a leak.</li>
            <li> Repair dripping faucets by replacing washers for greater home water saving. If your faucet is dripping at the rate of one drop per second, you can expect to waste 2,700 gallons per year which will add to the cost of water and sewer utilities, or strain your septic system.</li>
            <li> Check for toilet tank leaks by adding food coloring to the tank. If the toilet is leaking, color will appear within 30 minutes. Check the toilet for worn out, corroded or bent parts. Most replacement parts are inexpensive, readily available and easily installed. (Flush as soon as test is done, since food coloring may stain tank.)</li>
            <li> Avoid flushing the toilet unnecessarily. Dispose of tissues, insects and other such waste in the trash rather than the toilet.</li>
             <li>Take shorter showers. Replace you showerhead with an ultra-low-flow version. Some units are available that allow you to cut off the flow without adjusting the water temperature knobs.</li>

<li>Use the minimum amount of water needed for a bath by closing the drain first and filling the tub only 1/3 full. Stopper tub before turning water. The initial burst of cold water can be warmed by adding hot water later.</li>
<li>Don't let water run while shaving or washing your face. Brush your teeth first while waiting for water to get hot, then wash or shave after filling the basin.</li>
<li>Retrofit all wasteful household faucets by installing aerators with flow restrictors to help with your home water saving.</li>
<li>Operate automatic dishwashers and clothes washers only when they are fully loaded or properly set the water level for the size of load you are using.</li>
<li>When washing dishes by hand, fill one sink or basin with soapy water. Quickly rinse under a slow-moving stream from the faucet.</li>
<li>Store drinking water in the refrigerator rather than letting the tap run every time you want a cool glass of water.</li>
<li>Do not use running water to thaw meat or other frozen foods. Defrost food overnight in the refrigerator or by using the defrost setting on your microwave.</li>
<li>Kitchen sink disposals require lots of water to operate properly. Start a compost pile as an alternate method of disposing food waste instead of using a garbage disposal. Garbage disposals also can add 50% to the volume of solids in a septic tank which can lead to malfunctions and maintenance problems.</li>
<li>Consider installing an instant water heater on your kitchen sink so you don't have to let the water run while it heats up. This will reduce heating costs for your household.</li>
<li>Insulate your water pipes. You'll get hot water faster plus avoid wasting water while it heats up.</li>

<li>Never install a water-to-air heat pump or air-conditioning system. Air-to-air models are just as efficient and do not waste water.</li>
<li>Install water softening systems only when necessary. Save water and salt by running the minimum amount of regenerations necessary to maintain water softness. Turn softeners off while on vacation.</li>
<li>Check your pump. If you have a well at your home, listen to see if the pump kicks on and off while the water is not in use. If it does, you have a leak.</li>
<li>When adjusting water temperatures, instead of turning water flow up, try turning it down. If the water is too hot or cold, turn the offender down rather than increasing water flow to balance the temperatures.</li>
<li>If the toilet flush handle frequently sticks in the flush position, letting water run constantly, replace or adjust it.</li>
<li>Water your yard only before 8 a.m. to reduce evaporation and interference from wind.</li>
<li>Check your sprinkler system for leaks, overspray and broken sprinkler heads.</li>
