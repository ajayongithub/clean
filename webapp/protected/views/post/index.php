<p>A <b>Rich Internet Application (RIA)</b> is a Web application that
has many of the characteristics of desktop application software,
typically delivered by way of a site-specific browser, a browser
plug-in, an independent sandbox, extensive use of JavaScript, or a
virtual machine.<br>
Users generally need to install a software framework using the
computer's operating system before launching the application, which
typically downloads, updates, verifies and executes the RIA.<br>
<b>RIAs dominate</b> in online gaming as well as applications that
require access to video capture (with the notable exception of Gmail,
which uses its own task-specific browser plug-in). Web standards such as
HTML5 have developed and the compliance of Web browsers with those
standards has improved somewhat. However, the need for plug-in based
RIAs for accessing video capture and distribution has not diminished,
even with the emergence of HTML5 and JavaScript-based desktop-like
widget sets that provide alternative solutions for mobile Web browsing.
</p>
